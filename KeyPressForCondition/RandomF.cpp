DWORD RangedRand(DWORD range_min, DWORD range_max)
{
	static UCHAR randInit = 1;

	if (randInit)
	{
		srand(1792);
		randInit = 0;
	}

	DWORD r;
	r = ((double)rand() / RAND_MAX) * (range_max - range_min) + range_min;
	return r;
}